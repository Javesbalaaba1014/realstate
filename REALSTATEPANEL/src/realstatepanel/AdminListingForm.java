    public void populateJTable(){
    
        DataQuery dq = new DataQuery();
        ArrayList<housingdata> adlist = dq.HousingList();
        String[] colNames = {"propertyID","propertyType","Address","City","Province","nobedroom","nobathroom","propertysqft","propertyPrice","Description","LastUpdateAdmin","ownerID"};
        Object[][] rows = new Object[adlist.size()][12]; 
        
        for(int i = 0; i < adlist.size(); i++){
            rows[i][0] = adlist.get(i).getPropertyID();
            rows[i][1] = adlist.get(i).getPropertyType();
            rows[i][2] = adlist.get(i).getAddress();
            rows[i][3] = adlist.get(i).getCity();
            rows[i][4] = adlist.get(i).getProvince();
            rows[i][5] = adlist.get(i).getNobedroom();
            rows[i][6] = adlist.get(i).getNobathroom();
            rows[i][7] = adlist.get(i).getPropertysqft();
            rows[i][8] = adlist.get(i).getPropertyPrice();
            rows[i][9] = adlist.get(i).getDescription();
            rows[i][10] = adlist.get(i).getLastUpdateAdmin();
            rows[i][11] = adlist.get(i).getOwnerID();
      
           
        }
        MyModel  mmd = new MyModel(rows , colNames);
        JThousing.setModel(mmd);
        JThousing.setRowHeight(40);
        JThousing.getColumnModel().getColumn(11).setPreferredWidth(120);
    }
    
