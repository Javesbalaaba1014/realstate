    public boolean verifData(){

            if(JTFPropID.getText().equals("") && JTFPropType.getText().equals("")
                    || JTFaddress.getText().equals("") || JTFCity.getText().equals("")||
                    JTFProv.getText().equals("")|| JTFDescription.getText().equals("")
                    ||JTFlastAgent.getText().equals("") || JTFowner.getText().equals("")
                    || JTFPhone.getText().equals("") ||JTFbedroom.getText().equals("") || 
                    JTFbathroom.getText().equals("") || JTFSQFT.getText().equals("") 
                    || JTFPrice.getText().equals("") 
                     )
            {
                JOptionPane.showMessageDialog(null, "One or more Field are Empty!");
                return false;
            }
            else{
                return true;
            }
    }
